package MODEL;

/**
 *
 * @author Chiesaafandy
 */
public class Akun {
    private int id;
    private String nama, email, password, telephone;

    public Akun(int id, String nama, String email, String password, String telephone) {
        this.id = id;
        this.nama = nama;
        this.email = email;
        this.password = password;
        this.telephone = telephone;
    }

    public String getTelephone() {
        return telephone;
    }

    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }

    
    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }


}
