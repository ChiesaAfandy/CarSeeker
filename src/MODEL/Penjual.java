package MODEL;

/**
 *
 * @author Chiesaafandy
 */
public class Penjual {
    private int id;
    private String nama, telephone;

    public Penjual(int id, String nama, String telephone) {
        this.id = id;
        this.nama = nama;
        this.telephone = telephone;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getTelephone() {
        return telephone;
    }

    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }
    
    
}
